# Operadores de comparacion
# Los operadores de comparacion se utilizan para comparar dos valores y devolver un valor booleano (True o False) dependiendo del resultado de la comparacion.

print(1==1) # si es igual               = true
print(2!=1) # si es distinto            = true
print(2>1)  # si es mayor               = true
print(1<2)  # si es menor               = true
print(1>=1) # si es mayor o igual       = true
print(1<=1) # si es menor o igual       = true

